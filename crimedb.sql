CREATE TABLE COMPLAINT(
	COMPNO VARCHAR2(5) CONSTRAINT CNO_PK PRIMARY KEY,
	CNAME VARCHAR2(25),
	OCCUPATION VARCHAR2(20),
	DETAILS VARCHAR2(30),
	AGE NUMBER(2),
	SEX CHAR(1),
	CDATE DATE,
	NATIONALITY VARCHAR2(10)
	);


CREATE TABLE CSTATUS(
	COMPNO VARCHAR2(5) CONSTRAINT CNO_FK REFERENCES COMPLAINT(COMPNO),
	SDATE DATE,
	STATUS VARCHAR2(15)
	);

CREATE TABLE FIR(
	FIRNO VARCHAR2(6) CONSTRAINT FIR_PK PRIMARY KEY,
	COMPNO VARCHAR2(5) REFERENCES COMPLAINT(COMPNO),
	FIRDATE DATE,
	FIRTIME NUMBER(4,2),
	INFOTYPE VARCHAR2(20),
	PLACE VARCHAR2(15),
	ACT VARCHAR2(20),
	DISTR VARCHAR2(15),
	ADDR VARCHAR2(25),
	PASSPORTNO VARCHAR2(12),
	POLICENAME VARCHAR2(15)
	);


CREATE TABLE CHARGESHEET(
	CHRGSHNO VARCHAR2(6) CONSTRAINT chrgsh_PK PRIMARY KEY,
	POLSTNAME VARCHAR2(20),
	CHRGSHDATE  DATE,
	FIRNO VARCHAR2(6) REFERENCES FIR(FIRNO),
	DISTRICT VARCHAR2(15),
	INFONAME VARCHAR2(10),
	INFOADD VARCHAR2(30),
	INFOOCC VARCHAR2(15),
	INFORPARTICULARS VARCHAR2(50),
	ACCUSNAME VARCHAR2(12),
	ACCUSADD VARCHAR2(30),
	ACCUSSEX CHAR(1),
	ACCUSAGE NUMBER(2)
	);


